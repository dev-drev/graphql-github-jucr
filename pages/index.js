import Head from "next/head";

import { Box, FormControl, TextField } from "@mui/material";
import { GET_REPOSITORIES } from "../services/graphql/queries";
import theme from "../services/theme";
import { useState } from "react";
import DisplayData from "../components/DisplayData/DisplayData";
import Menu from "../components/Menu/Menu";

export default function Home() {
  const [section, setSection] = useState(GET_REPOSITORIES);
  const [query, setQuery] = useState("");

  return (
    <div>
      <Head>
        <title>GitHub GraphQL</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Box
        sx={{
          height: "100%",
          height: "100vh",
          display: "flex",
          backgroundColor: theme.palette.primary.dark,
        }}
      >
        <Menu section={section} setSection={setSection} />
        <Box sx={{ width: "70%" }}>
          <FormControl
            sx={{
              display: "flex",
              flexDirection: "column",
              alignItems: "center",
              justifyContent: "center",
            }}
          >
            <TextField
              sx={{
                backgroundColor: theme.palette.primary.light,
                width: "100%",
              }}
              id="outlined-basic"
              label="Search"
              variant="outlined"
              type="text"
              value={query}
              onChange={(e) => setQuery(e.target.value)}
            />
          </FormControl>
          <DisplayData query={query} section={section} />
        </Box>
      </Box>
    </div>
  );
}
